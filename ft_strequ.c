#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	char	*p1;
	char	*p2;
	int		i;

	p1 = (char*)s1;
	p2 = (char*)s2;
	i = 0;
	if (!s1 || !s2)
		return (0);
	if (p1[i] == '\0' && p2[i] == '\0')
		return (1);
	while (p1[i] == p2[i] && p1[i] && p2[i])
	{
		if (p1[i + 1] == '\0' && p2[i + 1] == '\0')
			return (1);
		i++;
	}
	return (0);
}
