#include "libft.h"

void		ft_putunbr(unsigned int n)
{
	if (n == 0)
		ft_putchar('0');
	if (n < 10 && n > 0)
		ft_putchar(n + '0');
	if (n >= 10)
	{
		ft_putunbr(n / 10);
		ft_putchar(n % 10 + '0');
	}
}
