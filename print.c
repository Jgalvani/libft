#include "libft.h"

void	print(void *p)
{
	int	len;

	if (write(1, p, 1) == -1)
		ft_putlim((intmax_t)p);
	else
	{
		len = ft_strlen(p) - 1;
		write(1, (p + 1), len);
	}
}
